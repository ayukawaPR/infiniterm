/**
 * Patches @homebridge/node-pty-prebuilt-multiarch to build on Windows
 * from MSYS2/bash environments where .bat file invocations fail in gyp.
 *
 * Issues fixed:
 *   1. GetCommitHash.bat — not invocable from MSYS2 context
 *   2. UpdateGenVersion.bat — same issue; GenVersion.h generated manually
 *   3. SpectreMitigation — VS2022 Preview requires Spectre libs by default
 */

const fs = require('fs');
const path = require('path');

const PKG = path.resolve(__dirname, '..', 'node_modules', '@homebridge', 'node-pty-prebuilt-multiarch');

if (!fs.existsSync(PKG)) {
  console.log('[patch-node-pty] Package not found, skipping.');
  process.exit(0);
}

let patched = 0;

// ---- 1. winpty.gyp: GetCommitHash.bat ----
const winptyGyp = path.join(PKG, 'deps', 'winpty', 'src', 'winpty.gyp');
if (fs.existsSync(winptyGyp)) {
  let content = fs.readFileSync(winptyGyp, 'utf8');
  let changed = false;

  if (content.includes('GetCommitHash.bat')) {
    content = content.replace(
      /'WINPTY_COMMIT_HASH%': '<!\(cmd \/c "cd shared && GetCommitHash\.bat"\)',/,
      "'WINPTY_COMMIT_HASH%': 'none',"
    );
    changed = true;
  }

  if (content.includes('UpdateGenVersion.bat')) {
    content = content.replace(
      /'<!\(cmd \/c "cd shared && UpdateGenVersion\.bat <\(WINPTY_COMMIT_HASH\)"\)',/,
      "'gen',"
    );
    changed = true;
  }

  if (changed) {
    fs.writeFileSync(winptyGyp, content, 'utf8');
    console.log('[patch-node-pty] Patched winpty.gyp (GetCommitHash + UpdateGenVersion)');
    patched++;
  }
}

// ---- 2. gen/GenVersion.h ----
const genDir = path.join(PKG, 'deps', 'winpty', 'src', 'gen');
const genHeader = path.join(genDir, 'GenVersion.h');
if (!fs.existsSync(genHeader)) {
  fs.mkdirSync(genDir, { recursive: true });
  fs.writeFileSync(genHeader, [
    '// AUTO-GENERATED by patch-node-pty.js',
    'const char GenVersion_Version[] = "0.4.4-dev";',
    'const char GenVersion_Commit[] = "none";',
    '',
  ].join('\n'), 'utf8');
  console.log('[patch-node-pty] Created gen/GenVersion.h');
  patched++;
}

// ---- 3. binding.gyp: disable SpectreMitigation ----
const bindingGyp = path.join(PKG, 'binding.gyp');
if (fs.existsSync(bindingGyp)) {
  let content = fs.readFileSync(bindingGyp, 'utf8');
  if (content.includes("'SpectreMitigation': 'Spectre'")) {
    content = content.replace(
      "'SpectreMitigation': 'Spectre'",
      "'SpectreMitigation': 'false'"
    );
    fs.writeFileSync(bindingGyp, content, 'utf8');
    console.log('[patch-node-pty] Patched binding.gyp (SpectreMitigation disabled)');
    patched++;
  }
}

if (patched === 0) {
  console.log('[patch-node-pty] Nothing to patch (already applied).');
} else {
  console.log(`[patch-node-pty] Done. ${patched} file(s) patched.`);
}
